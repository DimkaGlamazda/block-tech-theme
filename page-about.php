<div class="about-section-6">
  <div class="question">― サーシャ氏は過去にヨーロッパや米国でいくつかの事業立ち上げを経験していると聞きます。今回、日本に進出する理由はなんでしょう。</div>
  <div class="answer">
    <p>
      世界中のブロックチェーン事業者と交流していると不思議に思うことがあります。それは、世界有数の経済力を持っている先進国のうち、日本だけがブロックチェーンを積極的にビジネスに取り入れようとしていないからです。そして、その裏には、ビジネスに実装できるほどブロックチェーンを得意とする日本人技術者・有識者が大変不足しているから、という実態も突き止めました。ですから、私たちは世界を代表する経済大国「ニッポン」でありながらも、まだまだブロックチェーンの可能性が開かれていないにジャパン・マーケットに大変大きな関心と期待を寄せています。</p>
  </div>
  <div class="question">― それでは、BlockTechJapanの事業概要について教えてください。</div>
  <div class="answer">
    <p>
      当社の事業は、「一貫性」を重要視しています。ですから、ブロックチェーン導入に向けたセミナーからはじまり、その後は、コンサルティングやマーケティング、設計、開発、運用、教育、情報配信を一貫してオールインワンで行える事業体制を整えています。
      そうすることで、当社は、無駄なく矛盾なく、より効率的にブロックチェーンをお客様の組織内に導入していくことを実現します。</p>
  </div>
  <div class="question">― ブロックチェーン開発のサービスについて詳しく教えてください。</div>
  <div class="answer">
    <p>
      ブロックチェーンの導入は、各企業が目指す目標に合わせてカスタマイズ設計されるべきです。例えば、給与システムだけにブロックチェーンを導入したり、営業チームの管理だけにブロックチェーンを導入したり、組織内バラバラとした導入では十分な効果を期待できません。そのため、ブロックチェーンをトータルに実装できる設計力・開発力こそが当社の最大の強みです。プロジェクトはエンジニアの設計能力が高いほど有意義に進みます。もちろん案件の規模にもよりますが、通常のケースでは、1チーム12名体制で開発を行います。大規模なプロジェクトの場合には、お客様とも相談しながら、プロジェクトに参加する開発チームの数を増やしていきます。設計期間を除いた開発期間は、短いもので3か月、通常は半年~1年です。</p>
  </div>
  <div class="question">― 自社製品ではなく、ビジネス向けのブロックチェーン開発のサポートにフォーカスした理由はなんでしょう。</div>
  <div class="answer">
    <p>
      既存のマーケットに挑むベンチャー企業達は、様々な自社製品やサービスの開発を行っています。このような新しい組織では、プロダクトの開発スピードが大手企業の開発スピードを遥かに上回ります。しかし、ベンチャー企業の多くは未熟な組織体制と限られた資金リソースゆえに、世の中を変えることができる製品を完成させることがないまま、ビジネスの表舞台から退場することが多いです。</p>
    <p>
      そうした事実から、私たちは、世の中のために役立つ製品またはサービスの提供に携わりたいという思いから、すでにビジネスに長けた企業向けのブロックチェーン開発を行うことに特化し、大手企業でもスピーディーなブロックチェーン開発を実現できるようなサービスの提供に専念することにしています。</p>
    <p>
      また、これまでは「大企業＝信用＝高セキュリティ」という信用の構造があり、大手企業は大手企業にシステム開発の依頼をする傾向があったわけですが、ブロックチェーン開発の場合は、分散型ネットワークという性質もあり、私たちのような「開発者集団企業」が作ったブロックチェーンでも高い信頼性やセキュリティを確保できるためため、開発コストの安さやスピードの速さを重視して、案件を依頼してくれるクライアント様が増えているのを肌で感じるほどマーケットが変わってきています。</p>
  </div>
  <div class="question">―具体的にはどのような業界にサービスを提供したいと考えていますか。</div>
  <div class="answer">
    <p>世界各国、将来的には例外なくすべての産業分野にブロックチェーンが導入されていきます。インターネット・デバイス・IoTに関するテクノロジーの進化とともに、ブロックチェーンの需要はさらに高まります。
      実質改竄不可能な利用者同士の信用取引構造は、世界中の大半の人に便利で理想的だからです。したがって、我々が業界を選ぶという感覚はなく、どの業界が先に我々を選んでくれたかが重要になります。「世界を変えたい」という強いミッション感を持ち、ブロックチェーン導入のスピードを重視する組織にサービスを提供したいと考えています。</p>
  </div>
  <div class="question">―日本とのビジネスにおいて一番難しいところを教えてください。</div>
  <div class="answer">
    <p>
      日本人は、独特の商習慣があることで有名です。世界中の誰もが、日本でビジネスを行う難易度は高いと思っています。しかし、日本とのビジネスに成功し、軌道に乗せた企業は、その後世界各国でも成功し、長寿ビジネスになるという例も良く聞きます。</p>
    <p>慎重で用心深い日本人だからこそ、信用を得るまでのプロセスが最も難しく重要かと思っていますので、ブロックチェーンという新しい信用の築き方を通じ、積極的に日本人とのビジネスを進めていきたいと考えています。</p>
  </div>
  <div class="question">― 日本にオフィスをオープンする予定はありますか。</div>
  <div class="answer">
    <p>はい。今後の利便性を上げるために必要であれば、すぐにでも東京オフィスをオープンしたいと考えています。</p>
  </div>
  <div class="question">―今後の企業成長のために、どんなビジョンを描いていますか。</div>
  <div class="answer">
    <p>
      ほんの２０年前には小さなベンチャー企業でしかなかったいくつかの会社は、新しいツール「インターネット」を巧みに活用し、その企業サイズを怪物的なものに変え、世界に君臨しているのはなぜでしょうか。もともとは世界中みんなのために作られたインターネットは、このような企業に支配され、他者を寄せ付けませんでした。ブロックチェーンの登場により、少しずつではありますが「非中央集権」の流れができています。私たちは、これを加速させるべく、一強を目指さず、役割分担し、より多くの企業がブロックチェーンの利便性を享受できるようなサービスを幅広く提供していきたいと考えています。</p>
  </div>
</div>

