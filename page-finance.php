        <h4>4. 本人確認</h4>
        <p>本人確認なしにオンラインで金融取引を行うことはできません。しかし、本人確認は以下のような多くの手続きを踏む必要があります。</p>
        <ol>
          <li><strong>登録</strong>：なりすましを防ぐためにも、金融店舗でのリアル対面、身分証明書スキャンの送付、またはSkypeなどのオンライン会議を使った身元の確認が必要になります。</li>
          <li><strong>認証</strong>：銀行サービスにログインする度に、ユーザーが本人であることを証明する必要があります。</li>
          <li><strong>承認</strong>：取引を行う際に、追加の承認が求められることもあります。</li>
        </ol>
        <p>これらの全ての手続きは、新しいサービスプロバイダーを利用する度に行う必要があります。しかしブロックチェーンを使えば、他のサービスの本人確認情報を安全に再利用することが可能になります。</p>
        <ul>
          <li><strong>メリット</strong>：フィンテック分野でのブロックチェーンの利用により、ユーザーは自分自身の本人確認の方法、および本人情報を共有する相手を選ぶことができるようになります。本人情報をブロックチェーンに登録することは必要ですが、サービスプロバイダーごとに登録し直す必要はありません。ただし、それらのサービスプロバイダーもブロックチェーンを利用していることが条件です。
          </li>
          <li><strong>デメリット</strong>：ブロックチェーン上での本人確認のための基準は、まだ開発途中にあります。情報がブロックチェーンに登録された後は、ネットワークの全ての参加者がその情報にアクセスできるため、ユーザーは開示したくないプライベートな情報全てに制限をかける必要があります。
          </li>
          <li><strong>ユースケース</strong>：Cambridge
            BlockchainとTradleは、ブロックチェーンベースの顧客本人確認システムの開発に取り組み、ブロックチェーンを使って銀行業務を破壊しようとしているフィンテック・スタートアップの事例です。
          </li>
        </ul>
        <p>
          Tradleはブロックチェーンを利用してデータ認証の証明記録を保存すると共に、データの所有者がその完全な所有権を持ち、管理できるようにしています。このことは、顧客が銀行との情報共有を直接管理することを意味し、従って顧客はデータに関し万能になります。Tradleのアプローチによりデータ所有者は、さまざまなサービスプロバイダーに跨る自分のデータを、どんな国境も越えてあらゆる機関と共有することが可能になります。データの局所性に関する法律やGDPR等の規制に違反することも一切ありません。</p>
        <p>
          別の事例として、ID2020のケースが挙げられます。このプロジェクトは、従来の公式な本人確認書類を持たない人々のために、デジタル本人確認証を作ることを目的としています。このプロジェクトは、アクセンチュア、マイクロソフト、ロックフェラー財団から資金提供を受けています。</p>
