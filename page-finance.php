        <h4>7. 信用報告</h4>
        <p>
          ブロックチェーン金融は、個人や小規模企業がその信用履歴に基づいて、迅速に融資を受けられるようにすることもできます。借り手の信用履歴の審査は、貸し手にとって長い時間がかかる作業となる場合があります。第三者の信用調査機関が提供する従来の企業信用報告書は、小規模企業のオーナーには利用できません。また、企業にお金を払って機密データを利用するのは奇妙であり、安全性に欠けるように思われます。しかしブロックチェーンは、借り手が自らの信用報告をより正確で透明性のあるものにし、安全に共有することのできるツールを提供します。ブロックチェーンにより、以下のような仕組みが可能です。</p>
        <ol>
          <li>データ所有者が取引履歴をブロックチェーンの中に置き、秘密鍵で保護する。</li>
          <li>暗号化された取引がブロックチェーンの外に保存される。</li>
          <li>暗号化された取引はハッシュ化され、タイムスタンプおよびメタデータと共にブロックチェーンの内部に保存される。</li>
          <li>データの買い手が信用履歴の基準を提示する。</li>
          <li>スマートコントラクトがデータ所有者の管理基準に基づき、可能性のあるデータを識別し、検証する。</li>
          <li>ブロックチェーン エンジンがデータにフィルターをかけ、結果を返す。</li>
        </ol>
