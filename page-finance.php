        <h4>2. 有価証券の取引</h4>
        <p>これまで株や有価証券の売買には、仲介業者や証券取引所など、常にたくさんの第三者が関与してきました。通常、取引は以下の流れに従って行われます。</p>
        <ol>
          <li>買い手または売り手が取引の意思を第三者に示す。</li>
          <li>第三者である仲介業者が取引を証券取引所に送る。</li>
          <li>取引内容に基づいて取引相手が選定される。</li>
          <li>取引のリスク評価のため、取引内容が中央清算機関に送られる。</li>
          <li>買い手または売り手の仲介業者が中央証券保管機関と連携して、所有権の移転を申請する。</li>
          <li>取引内容が証券登録機関または証券代行業者に登録され、株主リストがアップデートされる。</li>
        </ol>
        <p>
          このように、伝統的な証券取引プロセスは複数のステップと煩雑な手続きを必要としており、取引が完了するまでに最大3日を要します。しかし、ブロックチェーンの分散的な性質を活用すれば、不要な仲介者が排除され、世界中のコンピューターで取引を実行することができます。そのため、相互接続ネットワークと連携するための中央サーバーもいらなくなるのです。</p>
        <ul>
          <li><strong>メリット</strong>：ブロックチェーン内での取引は情報の冗長性が軽減されるため、パフォーマンスが向上します。その結果、トレーダーのグループ間のより小規模な取引を、ブロックチェーンの外で素早く処理することができます。そして中間段階は一切省かれ、最終的な取引だけがブロックチェーンに記録されます。
          </li>
          <li><strong>デメリット</strong>：ブロックチェーンを使った取引はリスクを負う場合があります。その1つが「秘密鍵」です。秘密鍵はデジタル署名に利用される変数で、盗まれたり紛失したりする可能性があります。それらの鍵は特定資産の所有権を証明するものであるため、ハッカーは秘密鍵を盗みさえすれば、当該資産の所有権を変更できてしまいます。しかし、解決方法はあります。マルチシグネチャ取引です。マルチシグネチャ取引は、ブロックチェーン技術で実行されている資産取引アプリケーションに組み込むことができます。取引合意前に全ての当事者が署名することで、鍵を盗難から防ぎ、所有権が変更されるのを防止することができます。
          </li>
          <li><strong>ユースケース</strong>：世界で2番目に大きい証券取引所NASDAQは2015年に、そのプライベート・マーケット・プラットフォームでブロックチェーンを利用することを決定しました。NASDAQは、取引に使用されるコインとその他のコインを区別するのに役立つ、カラードコインのコンセプトを実装しました。さらにNASDAQは、シティグループと共同でブロックチェーンをベースにしたプラットフォームを開発しており、全ての取引と所有権の変更をリアルタイムに記録する、信頼性の高い分散型共有データベースを実現しようとしています。
          </li>
        </ul>
