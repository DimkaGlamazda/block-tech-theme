        <h4>6. 監査</h4>
        <img class="cases-finance-side-img-1" src="<?=Media::contentURI('samuel-zeller.jpg')?>" alt="samuel zeller">
        <p>
          おそらく、会計ほどペーパーワークの多い分野は他にありません。また、デジタル化の進み具合も比較的スローペースな分野です。その理由として、データの信頼性と完全性に関する厳格な規制要件が背景にあるのかもしれません。そのため会計は、コンプライアンス手続きの簡素化から伝統的な複式簿記まで、ブロックチェーン金融技術の力で変革することのできるもう1つの分野です。企業は領収書に基づいて個別に取引を記録する代わりに、共同記録簿に直接取引を書き込むことができます。その際に入力データは分散化され、暗号化により保護されます。その結果、記録の透明性が高まり、どのような偽造の試みもほぼ不可能になります。その仕組みは、取引を検証する「電子公証人」になぞらえることができます。また、ブロックチェーンのスマートコントラクトを利用して、請求書に対する支払いを自動化することもできます。</p>

        <ul>
          <li><strong>メリット</strong>：ブロックチェーンの助けを借りた標準化によって、監査人は財務諸表の背後にある最も重要なデータを自動的に検証できるようになり、従ってコストと時間を節約することが可能です。ブロックチェーンは電子ファイルの完全性を簡単に証明することができます。そのやり方の1つが、電子指紋を表すファイルのハッシュ文字列を生成し、それをブロックチェーンに書き込むことでファイルのタイムスタンプを作成することです。監査人はファイルの完全性を証明するために電子指紋を再生成し、ブロックチェーンに保存されている電子指紋と比較することができます。指紋が同一であれば、そのファイルが変更されていないことが証明されます。その結果、監査をリアルタイムに実施することができ、数日または数週間もかけずに済みます。
          </li>
          <li><strong>デメリット</strong>：ブロックチェーンは簿記のあらゆるケースに適しているとは言えず、銀行間送金のような特定の分野にだけ利用できると考える専門家もいます。</li>
          <li><strong>ユースケース</strong>：PwCは最近、企業がどのようにフィンテック ブロックチェーンを利用しているかチェックすることのできる、初めてのブロックチェーン監査サービスの開始を発表しました。
          </li>
        </ul>
