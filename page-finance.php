        <h4>5. シンジケートローン</h4>
        <p>
          シンジケートローン（協調融資）とは、貸し手がグループ（通常は銀行が貸し手となり、グループのことをシンジケートと呼ぶ）として個人に融資を提供することです。複数の参加者が関与するため、銀行が行う従来のシンジケートローンの手続きは、最大19日を要す場合があります。シンジケートローンを行う銀行は、以下のような課題に直面しています。</p>
        <ul>
          <li>ノウ・ユア・カスタマー（KYC）－ 顧客本人確認が必要。</li>
          <li>銀行秘密法（BSA）および反マネーロンダリング（AML）－ マネーロンダリング活動の防止、検知、報告を目的とした法的措置。</li>
        </ul>
        <p>
          ブロックチェーン金融サービスはこのプロセスを大幅に強化し、透明性をより高めることができます。ブロックチェーンの分散型台帳を使えば、シンジケート内の銀行は現地のコンプライアンス、KYC、またはBSA/AMLに関連するタスクを分散化し、それらを単独の顧客ブロックと結び付けることができます。</p>
        <ul>
          <li><strong>メリット</strong>：シンジケートローンのプロセスに金融ブロックチェーンを利用することで、シンジケートメンバーにはコンプライアンスに関する一連のメリットがもたらされます。シンジケート内で1つの銀行がブロックチェーンを使ってコンプライアンス手続きを完了させれば、他の全ての銀行が再び同じ手続きを行う必要はありません。その結果、各参加銀行はブロックチェーンを通して情報を交換することで、銀行業務においてブロックチェーン技術からメリットを享受することができるのです。これにより、シンジケートローンに求められる規制要件への対応コストを下げ、時間を大幅に節約できます。
          </li>
          <li><strong>デメリット</strong>：ブロックチェーンでは、シンジケートローン市場における全ての問題を解決することはできません。また、シンジケートローンにブロックチェーンを実装するのは、簡単ではないかもしれません。シンジケート内の全ての銀行で実装が必要となるためです。しかし最近では、銀行がこのプロセスを簡単にする目的で、ブロックチェーンシンジケートに参入する傾向にあります。
          </li>
          <li><strong>ユースケース</strong>：2016年にCredit
            Suisse、Symbiont、R3、およびIpreoが、シンジケートローン市場でのブロックチェーン技術の利用に関するプロジェクトにおいて、最初の段階を無事に完了させました。2018年4月には国際銀行7行、具体的にはBNPパリバ銀行、BNY
            Mellon、HSBC、ING、Natixis、およびステート・ストリート銀行が協力し、シンジケートローン向けのブロックチェーンプラットフォームであるFinastraのFusion
            LenderCommに投資しています。
          </li>
        </ul>
