        <h4>1. 決済</h4>
        <p>
          銀行や金融システムにおけるブロックチェーンの使用事例として真っ先に取り上げられるのが、決済に関するものです。世界中の中央銀行および商業銀行の両方が、決済処理に関してこの新たな技術を取り入れようとしており、独自のデジタル通貨を発行する可能性も検討しています。これには、これまではその大部分がSWIFTやWestern
          Unionにより支えられてきた、海外決済も含まれています。</p>
        <ul>
          <li><strong>メリット</strong>：海外決済にブロックチェーンを利用することで、従来のシステムよりも速く、安いコストで行うことができます。例えば、ブロックチェーンを使った送金コストは送金総額の2～3%なのに対し、他の第三者を経由して送金する場合、5～20%からの手数料が発生します。さらに、前述の通りブロックチェーンは第三者による承認を必要としないため、海外決済の処理速度を大幅にスピードアップすることができます。
          </li>
          <li><strong>デメリット</strong>：仮想通貨で海外決済を行う場合、セキュリティに関する特定のリスクを負う可能性があります。例えば、ブロックチェーン技術を利用した金融サービスやウォレットを使って仮想通貨をある国から別の国へ送る場合、もしサービスプロバイダーの1つが破産したり、ハッカーの攻撃を受けたりすれば、資金は失われ、銀行のような中央の権威者が当該損失を補償してくれることもありません。さらに、仮想通貨を送金先で現地通貨に交換する際に、交換レートの変動により問題が生じる場合もあります。
          </li>
          <li><strong>ユースケース</strong>：2016年にオーストラリア最大の銀行の1つWestpacが、国際決済の企業向けブロックチェーンソ
            リューションRippleと提携を結び、ブロックチェーン技術をベースとする低コストの国際決済システムを実装しました。2015年にはオーストラリアの別の大手銀行CBAが、支店間決済のための台帳システムをブロックチェーン上で開発するために、リップルとの提携を計画しました。2016年には米連邦準備銀行がIBMと協力し、ブロックチェーベースのデジタル決済システムを実装しようとしました。銀行がブロックチェーンを利用する事例はこれだけではありません。ブロックチェーン技術を取り入れようとしているその他の有名な銀行には、ドイツ銀行、バークレイズ銀行、BNPパリバ銀行などがあります。
          </li>
        </ul>
